-- 기본 테이블 생성
-- 브랜드, 차량명, 차량크기, 엔진CC, 가격, 런칭일

CREATE TABLE FIRST_PROJECT (
		BRAND VARCHAR(30) NOT NULL,
		CAR_NAME VARCHAR(30) NOT NULL,
		CAR_SIZE VARCHAR(30) NOT NULL,
		ENGIN_CC NUMBER,
		PRICE NUMBER NOT NULL,
		LAUNCH_DAY DATE,
		CONSTRAINT CARNAME_C UNIQUE(CAR_NAME)
);

SELECT * FROM FIRST_PROJECT;

-- 컬럼에 주석 추가
COMMENT ON COLUMN FIRST_PROJECT.BRAND  IS '브랜드';
COMMENT ON COLUMN FIRST_PROJECT.CAR_NAME  IS '차량명';
COMMENT ON COLUMN FIRST_PROJECT.CAR_SIZE  IS '차량크기';
COMMENT ON COLUMN FIRST_PROJECT.ENGIN_CC  IS '엔진CC';
COMMENT ON COLUMN FIRST_PROJECT.PRICE  IS '차량가격';
COMMENT ON COLUMN FIRST_PROJECT.LAUNCH_DAY  IS '런칭일';

